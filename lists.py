# list sclicing

t = [4, 19, 45, 67, 34, 78, 90, 100, 112]
slice1 = t[1:3]  # up to but not including index 3
print("slice 1:", slice1)

slice2 = t[:4]  # up to but not including index 4

print("Slice 2:", slice2)

slice3 = t[3:]  # index 3 to the end.

print(slice3)

